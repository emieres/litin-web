<p>	&nbsp;&nbsp;&nbsp;&nbsp;Apreciados Amigos Representantes:
Una vez reunido el Consejo Educativo con las autoridades administrativas del plantel, se realizo una revisión exhaustiva de los estados financieros, también se verifico la suma de los gastos de nómina y servicios que falta por cancelar para concluir el año escolar 2015-16, tomando en consideración la situación actual económica de las familias y la necesidad invertir en la educación de sus hijos. entendiendo que estamos en tiempo de crisis y que debemos ayudarnos a sobrellevar las cargas para que el colegio pueda seguir funcionando para el próximo año escolar, se acordó establecer una cuota única de Bs. 10.000,00 por estudiante, para cubrir los gastos de aumento de bono alimentación, aumento de sueldo mínimo, nóminas y servicios de los meses de mayo, junio, julio, agosto y liquidación y bono vacacional docente, así como también solventar la deuda que se arrastra con los docentes desde el mes de marzo cuando el ejecutivo nacional realizo incrementos salariales del 84%.<br><br>

&nbsp;&nbsp;&nbsp;&nbsp;Esta cuota puede ser cancelada en un pago único o en cuotas desde el 05/05/2016 hasta el 06/07/2016.<br><br>

&nbsp;&nbsp;&nbsp;&nbsp;Si usted requiere aclarar dudas, le agradecemos acudir en horas tempranas de la mañana para atenderle.</p>

