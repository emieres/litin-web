		<p>En el Litin, esta significativa frase del maestro Simón Rodríguez la hemos tomado como lema. Desde el año 1991 recibimos en nuestras aulas a jóvenes que han buscado respuesta a inquietudes, ávidos de querer desarrollarse de manera técnica y humana como bachilleres que se debían enfrentan a las expectativas del nuevo milenio. Cuando apenas se hablaba en nuestra sociedad de la necesidad de orientar a los muchachos hacia un desarrollo práctico, ya en 1989 el empresario Aníbal Gómez, Presidente del actual Consorcio Tecnológico, quien en su propósito de crear nuevos proyectos educativos acordes a la región, estudiaba la creación de una escuela técnica industrial, aunado a esto, en 1990 destacados educadores de la sociedad valenciana Rafael Henríquez, Freddy Hidalgo, Antonio Pérez, Edilberto Montilla, José Cazorla, Willians Bernal le presentaban su tesis de postgrado basado en modelo dual alemán, el cual plantea el aprendizaje en la fábrica como la mejor opción técnica profesional.</p>

		<p>Bajo la experiencia obtenida por el Consorcio Tecnológico para la Informática, en el desarrollo a nivel universitario de áreas técnicas, así como también enmarcándose en la imperiosa necesidad de cubrir la falta de escuelas técnicas y con la urgencia de crear a corto plazo instituciones que colaboraran con el país en la formación de bachilleres técnicos con experiencia en la industria; se crea la Unidad Educativa Liceo de Tecnología Industrial, con las especialidades de Electrónica y Administración Mención Informática, dando respuesta a la necesidad histórica de rescatar el concepto de las Escuelas Técnicas, adecuadas a la nueva realidad del país.</p>

		<p>Con el paso apresurado con el cual marcha nuestra sociedad, el Litin ha evolucionado, y de manera gradual, ha incursionado en nuevas áreas como la I y II Etapa de Educación Básica y la especialidad de Ciencias Básicas y Tecnología, orientando integralmente a nuestros alumnos desde 1er. grado hasta que egresan como bachilleres, en las áreas de electricidad, administración, informática, música, inglés, dibujo técnico aplicado, deportes entre otros. Manteniendo un estricto apego a los fundamentos que orientaron su creación, el Litin tiene como misión la formación de profesionales que aporten soluciones integrales de carácter interdisciplinario, basados en la investigación de acontecimientos científicos, administrativos, industriales, tecnológicos y humanos, promoviendo así el desarrollo integral de la sociedad. Con igual sentido se proyecta hacia el futuro con la visión de convertirse en una institución educativa de excelencia académica y profesional, líder en el estado Carabobo, en la formación técnica de bachilleres altamente competitivos en el campo científico, tecnológico e industrial; sí como también, seres proactivos y agentes de cambios en busca de obtener acreditación de normas de calidad en la investigación y dominio integral de la tecnología, la informática, la industria y la ciencia.</p>

		<p>Esta institución se ha mantenido en el desarrollo continuo de proyectos evaluando en cada uno de ello debilidades y fortalezas, que nos permiten dirigir el desarrollo por derroteros que nos encaminen hacia la excelencia. Entre los proyectos académicos mas representativos en el que la institución ha emprendido se encuentran los congresos anuales donde se abordan variadas temáticas enmarcadas en la Educación para el Trabajo y el desarrollo tecnológico, los Congresos de Sociedades Bolivarianas, Laboratorios  Experimentales de Estimulación y Creatividad, Preparadurías, Escuela de Emprendedores, Sala Telemática y Cine-Foro, Escuelas de Padres, Exposiciones anuales de propuestas y proyectos. De la misma manera y contando con el apoyo de la Fundación CTI se realizan actividades culturales que completan la formación intelectual, artística, vocacional y psicológica de los estudiantes, a través de la estudiantina, gimnasia rítmica, coral, banda Show, clubes deportivos, danza folklórica y contemporánea, etc...</p>

		<p>La creatividad no ha tenido límites, para los alumnos y docentes la libertad de innovar ha traído grandes logros, incorporar a sus alumnos a la experiencia profesional en las empresas más destacadas de la región, ha fortalecido este proyecto. Ver como se desenvuelven en universidades y organizaciones relevantes, como seres humanos útiles y productivos a la nación, nos ha marcado la nueva meta: Formar Emprendedores, jóvenes dueños de una educación digna, dispuestos en nuevo milenio, capaces de enfrentar las exigencias de un entorno lleno de oportunidades y de una sociedad ya no virtual, sino tangible, donde se ofertan las mejores oportunidades de estudios superiores, al que mejor se haya preparado en la educación básica; para de esta forma afrontarlas, desarrollarlas y aportar lo mejor de su talento en la creación del conocimiento y la construcción de un mundo mejor.</p>

		<p>La Unidad Educativa Litin, ha apreciado a lo largo de estos años con honda satisfacción, como cada una de las metas propuestas se han cumplido. Al observar la trayectoria de sus chicos desde que inician su primer grado, con facilidad discuten sus proyectos, exponen sus ideas y manifiestan la común frase ¡cuándo yo sea grande, quiero hacer!; ¿que harás?, lo que aprendí en el Litin, lo que mi maestro me enseñó. Crear, innovar y prepararme integralmente para ser emprendedor.</p>
